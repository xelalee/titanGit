DROP TABLE IF EXISTS GROUP_NAME;
CREATE TABLE GROUP_NAME
(
  GROUP_ID      INTEGER                          NOT NULL,
  GROUP_NAME    VARCHAR(64)
)CHARSET=utf8;

DROP TABLE IF EXISTS MENU;
CREATE TABLE MENU
(
  MENU_ID    INTEGER                             NOT NULL,
  MENU_NAME  VARCHAR(64),
